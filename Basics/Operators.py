# Arithmatic Operators '+' '-' '*' '/'
# Assignment operators '=' '+=' '-='
# Comparison operators '<' '>' '>=' '<=' '!=' '=='
# Logical operator 'AND' 'OR' 'NOT'
# to use logical operators use simply and or not keywords instead && || ! in c language


# Assignment
a = 5
b = 40
print(a, b)

# Arithmetic
print( a + b, a - b, a * b, b/a)

# Comparison returns boolean value
print( a == b, a > b, a < b )

# logical 
c = True or False
d = True and False
e = not True
print(c, d, e)